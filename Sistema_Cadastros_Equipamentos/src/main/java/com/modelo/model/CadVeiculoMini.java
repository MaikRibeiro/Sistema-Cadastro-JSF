package com.modelo.model;

public class CadVeiculoMini {
	
	private Integer cveiId = null;
	private String cveiPlaca = null;
	private String cveiDisplay = null;
	private Integer cveiTcorId = null;
	private String cveiTcorDescricao = null;
	
	public Integer getCveiId() {
		return cveiId;
	}
	
	public void setCveiId(Integer cveiId) {
		this.cveiId = cveiId;
	}
	
	public String getCveiPlaca() {
		return cveiPlaca;
	}
	
	public void setCveiPlaca(String cveiPlaca) {
		this.cveiPlaca = cveiPlaca;
	}
	
	public String getCveiDisplay() {
		return cveiDisplay;
	}
	
	public void setCveiDisplay(String cveiDisplay) {
		this.cveiDisplay = cveiDisplay;
	}
	public Integer getCveiTcorId() {
		return cveiTcorId;
	}
	public void setCveiTcorId(Integer cveiTcorId) {
		this.cveiTcorId = cveiTcorId;
	}
	public String getCveiTcorDescricao() {
		return cveiTcorDescricao;
	}
	public void setCveiTcorDescricao(String cveiTcorDescricao) {
		this.cveiTcorDescricao = cveiTcorDescricao;
	}

}
